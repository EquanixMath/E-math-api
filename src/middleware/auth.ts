/**
 * Middleware ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö JWT token
 */
export const authMiddleware = async (req: AuthRequest, res: Response, next: NextFunction) => {
  try {
    // ‡∏î‡∏∂‡∏á token ‡∏à‡∏≤‡∏Å Authorization header
    const authHeader = req.headers.authorization;
    if (!authHeader || !authHeader.startsWith('Bearer ')) {
      return res.status(401).json({ message: '‡πÑ‡∏°‡πà‡∏û‡∏ö Token ‡∏Å‡∏≤‡∏£‡∏¢‡∏∑‡∏ô‡∏¢‡∏±‡∏ô‡∏ï‡∏±‡∏ß‡∏ï‡∏ô' });
    }

    const token = authHeader.split(' ')[1];

    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤ token ‡∏≠‡∏¢‡∏π‡πà‡πÉ‡∏ô blacklist ‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
    console.log('üîç Checking blacklist for token:', token.substring(0, 20) + '...');
    const blacklisted = await BlacklistedToken.findOne({ token });
    console.log('üîç Blacklist result:', blacklisted ? 'FOUND (REVOKED)' : 'NOT FOUND (OK)');
    
    if (blacklisted) {
      console.log('‚ùå Token is blacklisted, rejecting request');
      return res.status(401).json({ message: 'Token ‡∏ñ‡∏π‡∏Å‡∏¢‡∏Å‡πÄ‡∏•‡∏¥‡∏Å‡πÅ‡∏•‡πâ‡∏ß ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡πÄ‡∏Ç‡πâ‡∏≤‡∏™‡∏π‡πà‡∏£‡∏∞‡∏ö‡∏ö‡πÉ‡∏´‡∏°‡πà' });
    }

    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡πÅ‡∏•‡∏∞ decode JWT token
    const decoded = jwt.verify(token, JWT_SECRET) as any;
    
    // ‡πÄ‡∏Å‡πá‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• user ‡πÉ‡∏ô request object
    req.user = {
      id: decoded.id,
      username: decoded.username,
      role: decoded.role,
      status: decoded.status
    };

    console.log('‚úÖ Token verified for user:', decoded.username, 'role:', decoded.role);
    next();
  } catch (err) {
    console.error('‚ùå Token verification failed:', err);
    
    if (err instanceof jwt.TokenExpiredError) {
      return res.status(401).json({ message: 'Token ‡∏´‡∏°‡∏î‡∏≠‡∏≤‡∏¢‡∏∏ ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡πÄ‡∏Ç‡πâ‡∏≤‡∏™‡∏π‡πà‡∏£‡∏∞‡∏ö‡∏ö‡πÉ‡∏´‡∏°‡πà' });
    } else if (err instanceof jwt.JsonWebTokenError) {
      return res.status(401).json({ message: 'Token ‡πÑ‡∏°‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á' });
    }
    
    return res.status(401).json({ message: '‡∏Å‡∏≤‡∏£‡∏¢‡∏∑‡∏ô‡∏¢‡∏±‡∏ô‡∏ï‡∏±‡∏ß‡∏ï‡∏ô‡∏•‡πâ‡∏°‡πÄ‡∏´‡∏•‡∏ß' });
  }
};

